<?php

declare(strict_types=1);

namespace Nikcrazy37\Hw16\Modules\Statement\Domain;

readonly class User
{
    private string $name;

    /**
     * @param string $name
     */
    public function __construct(string $name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }
}